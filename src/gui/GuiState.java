package gui;

/**
 * Enum that represents the different states of the GUI.
 */
public enum GuiState {
    /**
     * The state of the GUI when the current table is the book table.
     */
    BOOKS,
    /**
     * The state of the GUI when the current table is the user table.
     */
    USERS,
    /**
     * The state of the GUI when the current table is the history table.
     */
    HISTORY
};

